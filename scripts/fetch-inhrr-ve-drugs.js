// scripts/fetch-inhrr-ve-drugs.js
// Baixa medicamentos registrados no INHRR (Venezuela) e gera drug-catalog-ve-inhrr.js
// Fonte p?blica: https://inhrr.gob.ve/fichasfarma/formweb/buscar_medicamento.php
//
// Uso: node scripts/fetch-inhrr-ve-drugs.js
//      node scripts/fetch-inhrr-ve-drugs.js --letters=A,B   (subconjunto para teste)

const fs = require("fs");
const https = require("https");
const path = require("path");

const BASE =
  "https://inhrr.gob.ve/fichasfarma/formweb/buscar_medicamento.php";
const OUT = path.join(__dirname, "drug-catalog-ve-inhrr.js");
const DELAY_MS = 400;

const ALL_LETTERS = "ABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789".split("");

function sleep(ms) {
  return new Promise((r) => setTimeout(r, ms));
}

function fetchUrl(url) {
  return new Promise((resolve, reject) => {
    const req = https.get(
      url,
      {
        headers: {
          "User-Agent": "Doctor8/1.0 (humanitarian catalog import; contact@doctor8.com)",
          Accept: "text/html",
        },
        timeout: 60000,
      },
      (res) => {
        const chunks = [];
        res.on("data", (c) => chunks.push(c));
        res.on("end", () => resolve(Buffer.concat(chunks).toString("latin1")));
      }
    );
    req.on("error", reject);
    req.on("timeout", () => {
      req.destroy();
      reject(new Error("timeout"));
    });
  });
}

function pageUrl(letter, page) {
  const params = new URLSearchParams({
    Campo: "producto",
    Condicion: "inicia_con",
    Termino: letter,
    orden: "producto",
    pagina: String(page),
  });
  return `${BASE}?${params}`;
}

function parseRows(html) {
  const rows = [];
  const parts = html.split(/<tr>/i);
  for (const part of parts) {
    if (!part.includes("</tr>")) continue;
    const tds = [...part.matchAll(/<td[^>]*>([\s\S]*?)<\/td>/gi)].map((m) =>
      m[1].replace(/<[^>]+>/g, "").replace(/\s+/g, " ").trim()
    );
    if (tds.length < 5) continue;
    if (!tds[0] || tds[0].includes("No.Reg")) continue;
    rows.push({
      nrs: tds[0],
      name: tds[1],
      activeIngredient: tds[2],
      manufacturer: tds[3] || null,
      status: tds[4],
    });
  }
  return rows;
}

function hasNextPage(html, currentPage) {
  const nextPage = currentPage + 1;
  const re = new RegExp(
    `buscar_medicamento\\.php\\?[^"'\\s]*pagina=${nextPage}[^"'\\s]*`,
    "i"
  );
  return re.test(html);
}

async function fetchLetter(letter, allByNrs) {
  let page = 1;
  let added = 0;
  const MAX_PAGES = 200;

  while (page <= MAX_PAGES) {
    const url = pageUrl(letter, page);
    let html;
    try {
      html = await fetchUrl(url);
    } catch (err) {
      console.warn(`  [${letter}] pag ${page} falhou: ${err.message}`);
      break;
    }

    const rows = parseRows(html);
    if (!rows.length) break;

    let pageAdded = 0;
    for (const row of rows) {
      if (row.status !== "VIGENTE") continue;
      if (!row.name || allByNrs.has(row.nrs)) continue;
      allByNrs.set(row.nrs, {
        nrs: row.nrs,
        name: row.name,
        activeIngredient: row.activeIngredient || row.name,
        presentation: row.name,
        manufacturer: row.manufacturer,
      });
      added++;
      pageAdded++;
    }

    if (!pageAdded && page > 1) break;

    if (!hasNextPage(html, page)) break;
    page++;
    await sleep(DELAY_MS);
  }

  return added;
}

async function main() {
  const argLetters = process.argv
    .find((a) => a.startsWith("--letters="))
    ?.split("=")[1]
    ?.split(",")
    .map((s) => s.trim().toUpperCase())
    .filter(Boolean);

  const letters = argLetters?.length ? argLetters : ALL_LETTERS;
  const allByNrs = new Map();

  console.log(`INHRR Venezuela ? buscando ${letters.length} prefixos...`);

  for (const letter of letters) {
    const n = await fetchLetter(letter, allByNrs);
    console.log(`  ${letter}: +${n} vigentes (total ?nico ${allByNrs.size})`);
    await sleep(DELAY_MS);
  }

  const drugs = [...allByNrs.values()].sort((a, b) =>
    a.name.localeCompare(b.name, "es")
  );

  const body =
    `// Auto-generated by scripts/fetch-inhrr-ve-drugs.js\n` +
    `// Source: INHRR ? Productos Farmac?uticos Registrados (status VIGENTE)\n` +
    `// ${new Date().toISOString()}\n` +
    `module.exports = ${JSON.stringify(drugs, null, 2)};\n`;

  fs.writeFileSync(OUT, body, "utf8");
  console.log(`\nSalvo ${drugs.length} medicamentos em ${OUT}`);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
