export const dictionaries: Record<Lang, LocaleDict> = { en: en as LocaleDict, pt, es };

export function translate(lang: Lang, key: TranslationKey): string;
export function translate(lang: Lang, key: string): string;
export function translate(lang: Lang, key: string): string {
  const d = dictionaries[lang] || en;
  return (d as Record<string, string>)[key] ?? (dictionaries.en as Record<string, string>)[key] ?? key;
}

// Server-safe helpers (usable in Server Components) -----------------

export function normalizeLang(value: string | null | undefined): Lang {
  if (value === "pt" || value === "en" || value === "es") return value;
  return "en";
}

// Locale code for date/number formatting based on language
export function localeOf(lang: Lang): string {
  return lang === "pt" ? "pt-BR" : lang === "es" ? "es-ES" : "en-US";
}

/** Formats a slot count with correct singular/plural (e.g. "1 horário" vs "5 horários"). */
export function formatSlotCount(lang: Lang, count: number): string {
  if (count <= 0) return "";
  if (lang === "pt") {
    return count === 1 ? "1 horário" : `${count} horários`;
  }
  if (lang === "es") {
    return count === 1 ? "1 horario" : `${count} horarios`;
  }
  return count === 1 ? "1 slot" : `${count} slots`;
}

// Time-of-day greeting key
export function greetingKey(date = new Date()): TranslationKey {
  const h = date.getHours();
  if (h < 12) return "greeting.morning";
  if (h < 18) return "greeting.afternoon";
  return "greeting.evening";
}
